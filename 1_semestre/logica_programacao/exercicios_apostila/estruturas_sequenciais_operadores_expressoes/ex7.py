"""
Construa um fluxograma que permite ao usuário digitar dois números inteiros
e exibir o resultado para cada uma das seguintes operações: +, -, *, /, //, %, **.
"""

y = int(input('Digite o primeiro número: '))
x = int(input('Digite o segundo número: '))

print('a)', y + x)
print('b)', y - x)
print('c)', y * x)
print('d)', y / x)
print('e)', y // x)
print('f)', y % x)
print('g)', y ** x)
